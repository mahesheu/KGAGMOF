<h1>Who we are</h1>
<p>
KGAGMOF is the most prominent and dominant grouping among Medical Officers in the department of Indian systems of Medicine. It came out as a product resulting from the incessant step motherly attitude meted out to the degree holding medical officers by the concerned authorities of the ISM Department. At that time. The ‘Uncle Sam ‘attitude of so called ‘the first and oldest ‘grouping triggered the graduate holders to uphold and safeguard their dignity and self respect to break away and form a new group. The sole aim of ‘the other group’ was to do favours to those members, majority of whom were having educational qualifications ranging from 3rd STD – 7th STD and those who got promoted as Medical Officers from non-cadre. They gave scant regard not only to graduate medical officers but also to those Medical Officers who had a regular post graduate Diploma from a recognized academic institution and was recruited by Kerala PSC. Things came to such an extent that almost all senior positions in the Dept. (SMO, CMO, Supdt, DMO, JD, and DISM) became the monopoly of Diploma holders. Gross majority of graduate Medical Officers who enter into the service had to retire as Medical Officers, even after putting in more than 20 years of service. To put an end to this type of ridiculous, callous and cunning policy, graduate medical officers had no other option but to revolt against this gross injustice. It is a matter of  immense pride that only due to the relentless effort put by KGAGMOF, Medical Officers with graduate degree in Ayurveda were given professional parity with that of Assistant Surgeons of health Services not only once but twice, but that too was sabotaged and thwarted by some elements with vested interests in ‘the other group’
</p>
<div id="history">
<h1>Background</h1> 
</div>
<p>The basic qualification of the medical officers in Dept of ISM was changed to basic degree in Ayurveda in 1977,by which the 3rd pay revision commission of 1978 gave parity to MOs of ISM with that of MOs in Dept of Health Services (Rs.600-1100 scale of pay).But some vested interests on behalf of some Diploma personnel who were influential, the basic qualification was again changed in to diploma in Ayurveda in 1983,as a result of which ,the succeeding pay rev.commission (4th PRC)of 1988,denied the existing parity citing the difference regarding entry level qualification. As the then existing association of MOs of that time were reluctant to oppose this, it became apparent, that to uphold the diginity, status and self respect of the MOs possessing graduation, it was needed to have a separate grouping. It is at this juncture the idea of KGAGMOF was originated.</p>
<p>
A meeting was convened on May 10th,1981 at Sangeetha Tourist Home in Ernakulam under the efficient leadership of Dr.T.N.Yatheendran and Dr.Sreekumaran Thampi.The launching of KGAGMOF was done with the pledge to safe guard the self respect and pride of Degree Medical Officers. The first state conference of KGAGMOF was held on 28-01-1984 at Bini Tourist Home, Thrissur. Dr.P.A.George and Dr.Premlal were elected as precident and secretary respectively from the conference. The state committee gave memorandum to Government of the Kerala demanding on pay parity with MOs in DHS.
</p><p>
Official recognition came in 1985 (SI No.242 GO (P) No.7/85 GAD Tvm dtd.3-1-1985).In reply to federation’s memorandum; the Govt.of Kerala in 1986 gave the following reply.’ Since the basic qualification for the entry post for medical officer(ISM) and medical officer (DHS) are not same and comparable, parity in salary is not possible’. But the 7th Estimates Committee of Kerala Legislature taking into consideration the diversified and special nature of ISM, suggested that the Director and Joint Director may be given more Financial and Administrative powers and to these posts, the minimum essential qualification should be graduation in Ayurveda.
</p><p>
In 1986,KGAGMOF approached the Hon’ble High Court of Kerala and filed a case on the issue of parity(O.P.no.140/86).The Hon’be High Court in 1988 gave the verdict to 5th Pay Revision Commission to give parity to medical officers of ISM Dept possessing graduate degree in Ayurveda (to that of MOs of DHS) .Based on the above verdict, PRC recommended Parity and the Government gave Parity to medical officers of ISM Dept with graduation in Ayurveda .The scale of both Ay.Medical Officers and Modern Medicine Medical Officers became identical (Rs.1480-2825).The scale of medical officers of ISM Dept with a Diploma in Ayurveda was fixed at Rs.1330-2550.Please note that ,at that period, the gazetted scale started from Rs.1450-2825.In 1995,Diploma Association demanded that the parity enjoyed by the graduate medical officers may be extended to them also. They also approached the Hon’ble High Court with this demand. In 1997, KGAGMOF thought over this issue and came to the conclusion that, if Diploma holders stand a chance to get parity with them, let them have it. Because of this benevolent view KGAGMOF did not challenge them in court. Due to this, Diploma holders also got the same salary as that of Degree Medical officers. But in the mean time these benevolent view of Federation turned against us. Though the pay scale medical officers were placed in the gazette scale, they were not given gazetted status. But ‘certain’ associations declared that MOs are gazette spread the news that the non issual of pay slip from Accountant General was due to some technical reasons.
</p><p>
Federation realised that unless and until the special rules are amended and modified, gazetted rank will not be achieved and started several intensive measures. Federation tried its best to bring pressure on the Department and Government to initiate steps in this regard. But not much progress could be made owing to stiff resistance from vested interest from different quarters. Atlast, Federation in 1997 filed an OP in the Hon’ble High Court of Kerala (OP No.16241/1997).The Diploma association fought it with tooth and nail to sabotage it for fearing that all their existing benefits will lose if special rules were implemented. But the Hon’ble High Court of Kerala approved Federations stand and gave necessary directions to the Government to initiate proceedings regarding modification special rules. Since the personnel at the helm of ISM Dept belonged to the opposite side, scant regard was given to the Court directions to Government. Federation was forced to file contempt of court (W.A.1320/2003) against the Department and the Government due to their indifferent attitude and apathy. The Hon’be High Court effectively intervened directing the Chief Secretary of Kerala to modify special rules within a period of three months(9-2-2004).But the Government submitted an affidavit requesting the court to grant 6 months time.
</p><p>
Federation realized that the additional time requested by is a part of the wider game of the department and Government to prolong the modification of special rule and again approached Court. On 13-03-2004 after hearing the plea from Federation, the Hon’ble High Court opined that the time asked by the Govt. is unduly long and there is no merit in it. The Court issued order granting only three months for the special rule modification process. Federation was forced to approach the Hon’ble High Court ( W.P (C) No.14523/04 ) again due to the non compliance of the Department and the Government regarding - the observation and opinion of Estimates Committees of 1986-87,1998-2000,2000-2001,Directions of Hon’ble High Court (OP No.16241/1997) and (W.A.1320/2003) in giving due promotions and other service benefits to graduate medical officers. The Department Heads of those times were purposely delaying rule modification procedures to give Diploma medical officers benefits out of their turn, thus making a mockery at the graduate medical officers. Again due to the timely intervention by the Hon’ble High court Federation was able to get favourable orders.
</p><p>
After a long and historic fight, the fruits of our labour came in the form of (G.O.(P)No93/2005 H & FWD Tvpm dated 05-04-2005) also SRONo.350/2005.Yes! Special rule was modified and gazetted rank of medical officers became a reality, pay slips fro accountant general were also issued accordingly. But due to some vested interested personnel at the top hierarchy of ISM Department, a utopian note was included in the special rule citing that for all purposes, Diploma will be considered as equal to Degree in case of existing in-service medical officers having diploma. Federation has approached the Hon’ble High Court to remove this biased note from Special rule. </p>
